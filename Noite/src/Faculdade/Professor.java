public class Professor {
	
	public String nome;
	public String cpf;
	public String email;
	public String matricula;
	
	
	public Professor(String nome,String cpf,String email,String matricula) {
		
		this.nome=nome;
		this.cpf=cpf;
		this.email=email;
		this.matricula=matricula;
		
	}

}
